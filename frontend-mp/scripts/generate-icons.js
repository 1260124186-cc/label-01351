const fs = require('fs');
const path = require('path');

// 简单的 1x1 像素 PNG（灰色和棕色）
const grayPixel = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
  0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,
  0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,
  0xDE, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41,
  0x54, 0x08, 0xD7, 0x63, 0x90, 0x90, 0x90, 0x00,
  0x00, 0x02, 0x10, 0x01, 0x00, 0xA1, 0xFD, 0x2C,
  0x4E, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E,
  0x44, 0xAE, 0x42, 0x60, 0x82
]);

const brownPixel = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
  0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,
  0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,
  0xDE, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41,
  0x54, 0x08, 0xD7, 0x63, 0x88, 0x44, 0x14, 0x00,
  0x00, 0x02, 0x19, 0x01, 0x06, 0x2E, 0x8D, 0x5B,
  0x3A, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E,
  0x44, 0xAE, 0x42, 0x60, 0x82
]);

const iconsDir = path.join(__dirname, '..', 'assets', 'icons');

// 确保目录存在
if (!fs.existsSync(iconsDir)) {
  fs.mkdirSync(iconsDir, { recursive: true });
}

// 生成图标文件
const icons = ['home', 'publish', 'mine'];
icons.forEach(name => {
  fs.writeFileSync(path.join(iconsDir, `${name}.png`), grayPixel);
  fs.writeFileSync(path.join(iconsDir, `${name}-active.png`), brownPixel);
});

console.log('图标文件已生成到 assets/icons/ 目录');

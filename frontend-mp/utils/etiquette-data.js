const getSceneList = function() {
  return SCENES.map(function(s) {
    return { id: s.id, name: s.name, icon: s.icon, desc: s.desc };
  });
};

const getSceneById = function(id) {
  return SCENES.find(function(s) { return s.id === id; }) || null;
};

const getRegionList = function() {
  return REGIONS.map(function(r) {
    return { id: r.id, name: r.name, provinces: r.provinces };
  });
};

var findRegionByProvince = function(province) {
  if (!province) return null;
  return REGIONS.find(function(r) {
    return r.provinces.some(function(p) { return province.indexOf(p) > -1 || p.indexOf(province) > -1; });
  }) || null;
};

var getDefaultEtiquette = function() {
  return JSON.parse(JSON.stringify(DEFAULT_ETIQUETTE));
};

var initEtiquetteData = function() {
  var stored = wx.getStorageSync('etiquette');
  if (!stored || stored.length === 0) {
    wx.setStorageSync('etiquette', getDefaultEtiquette());
  }
  var favStored = wx.getStorageSync('etiquetteFavorites');
  if (!favStored) {
    wx.setStorageSync('etiquetteFavorites', {});
  }
};

module.exports = {
  SCENES: SCENES,
  REGIONS: REGIONS,
  DEFAULT_ETIQUETTE: DEFAULT_ETIQUETTE,
  getSceneList: getSceneList,
  getSceneById: getSceneById,
  getRegionList: getRegionList,
  findRegionByProvince: findRegionByProvince,
  getDefaultEtiquette: getDefaultEtiquette,
  initEtiquetteData: initEtiquetteData
};

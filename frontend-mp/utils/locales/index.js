let currentLang = LANG_ZH_CN;

function getStoredLang() {
  try {
    const stored = wx.getStorageSync(LANG_KEY);
    if (stored && (stored === LANG_ZH_CN || stored === LANG_ZH_TW)) {
      return stored;
    }
  } catch (e) {}
  return LANG_ZH_CN;
}

function setStoredLang(lang) {
  try {
    wx.setStorageSync(LANG_KEY, lang);
  } catch (e) {}
}

function initI18n() {
  currentLang = getStoredLang();
  return currentLang;
}

function getLang() {
  return currentLang;
}

function setLang(lang) {
  if (lang !== LANG_ZH_CN && lang !== LANG_ZH_TW) {
    lang = LANG_ZH_CN;
  }
  currentLang = lang;
  setStoredLang(lang);
  return currentLang;
}

function t(key, params = {}) {
  const keys = key.split('.');
  let value = resources[currentLang];
  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k];
    } else {
      value = resources[LANG_ZH_CN];
      for (const k2 of keys) {
        if (value && typeof value === 'object' && k2 in value) {
          value = value[k2];
        } else {
          return key;
        }
      }
      break;
    }
  }
  if (typeof value === 'string') {
    return value.replace(/\{(\w+)\}/g, (_, name) => {
      return params[name] !== undefined ? String(params[name]) : `{${name}}`;
    });
  }
  return key;
}

function scToTc(text) {
  if (!text || typeof text !== 'string') return '';
  let result = '';
  for (let i = 0; i < text.length; i++) {
    const char = text[i];
    result += SC_TO_TC_MAP[char] || char;
  }
  return result;
}

function tcToSc(text) {
  if (!text || typeof text !== 'string') return '';
  const TC_TO_SC_MAP = {};
  for (const sc in SC_TO_TC_MAP) {
    TC_TO_SC_MAP[SC_TO_TC_MAP[sc]] = sc;
  }
  let result = '';
  for (let i = 0; i < text.length; i++) {
    const char = text[i];
    result += TC_TO_SC_MAP[char] || char;
  }
  return result;
}

function translateText(text) {
  if (!text || typeof text !== 'string') return '';
  if (currentLang === LANG_ZH_TW) {
    return scToTc(text);
  }
  return text;
}

function getLangOptions() {
  return [
    { value: LANG_ZH_CN, name: resources['zh-CN'].settings.languageSC },
    { value: LANG_ZH_TW, name: resources['zh-TW'].settings.languageTC }
  ];
}

function getLangName(lang) {
  const options = getLangOptions();
  const opt = options.find(o => o.value === lang);
  return opt ? opt.name : resources['zh-CN'].settings.languageSC;
}

